WITH HISTORYCOND AS(
    SELECT 
        CAR_ID,
        START_DATE,
        END_DATE
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE NOT((START_DATE<'2022-11-01' AND END_DATE<'2022-11-01')
    OR START_DATE>'2022-11-30')
)

SELECT 
    DISTINCT C.CAR_ID,
    C.CAR_TYPE,
    FLOOR(C.DAILY_FEE * 30 * (1-(P.DISCOUNT_RATE/100))) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
    LEFT JOIN HISTORYCOND H
        ON C.CAR_ID = H.CAR_ID
    LEFT JOIN (
        SELECT *
        FROM CAR_RENTAL_COMPANY_DISCOUNT_PLAN
        WHERE CAR_TYPE IN ('세단','SUV')
            AND DURATION_TYPE LIKE '30%'
    ) P
        ON C.CAR_TYPE=P.CAR_TYPE
WHERE (C.DAILY_FEE * 30 * (1-(P.DISCOUNT_RATE/100)) >=500000
       AND C.DAILY_FEE * 30 * (1-(P.DISCOUNT_RATE/100)) <2000000)
       AND H.CAR_ID IS NULL
ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC;