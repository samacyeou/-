SELECT 
    B.TITLE,
    B.BOARD_ID,
    R.REPLY_ID,
    R.WRITER_ID,
    R.CONTENTS,
    date_format(R.CREATED_DATE, '%Y-%m-%d') as CREATED_DATE
from USED_GOODS_BOARD B, USED_GOODS_REPLY R
where B.CREATED_DATE like "2022-10%"
    and B.BOARD_ID = R.BOARD_ID
order by R.created_date asc, B.title asc